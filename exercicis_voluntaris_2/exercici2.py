color = input('Color: ')

if color.lower() == 'roig':
    print('PASSIÓ')
elif color.lower() == 'groc':
    print('FELICITAT')
elif color.lower() == 'verd':
    print('ESPERANÇA')
elif color.lower() == 'blau':
    print('CALMA')
elif color.lower() == 'morat':
    print('CREATIVITAT')
else:
    print('ERROR')
